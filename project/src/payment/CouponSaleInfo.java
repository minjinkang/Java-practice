package payment;

public class CouponSaleInfo {
	private String couponNO;
	private String sale;
	
	public String getCouponNO() {
		return couponNO;
	}
	public void setCouponNO(String couponNO) {
		this.couponNO = couponNO;
	}
	public String getSale() {
		return sale;
	}
	public void setSale(String sale) {
		this.sale = sale;
	}
	
}
